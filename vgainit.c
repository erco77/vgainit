// vgainit.c - MS-DOS/Turbo C tool to initialize the VGA registers to a 60Hz 80x25 mode
//
// I wrote this to get a particular LCD display that could only display 60Hz to work
// on a machine whose onboard VGA BIOS would only generate 70Hz for text modes.
//
// A lot of this code turned out not to be necessary (the font and palette stuff),
// so I'll probably do a pass to rip that stuff out. Leaving it in for the first
// commit in case it's useful for other purposes.
//
// This tool directly writes to the VGA registers to force it into a particular 
// mode that I needed. Might be a good starting point for a more flexible tool.
// An important part of this project was needing to disable the Protect bit in the
// CRTC register. My card had that set, and prevented other tools from changing
// the modes..!				-erco@seriss.com 03/28/2024

#include <stdio.h>
#include <memory.h>
#include <conio.h>	// inp,outp
#undef outp		// TC needs this

// Note: Code below: outb() has 'value' as first parameter, 'port' as second.>
//       TC's outp() function is reverse: 'port' first, 'value' second.
//
char inb(int port)           { return inp(port); }
void outb(int val, int port) { outp(port, val); }

